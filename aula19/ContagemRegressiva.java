/* Nesse programa vamos usar a estrutura 'for' para imprimir
 * uma contagem regressiva do 10 atÃ© o 1
 */

public class ContagemRegressiva {
    
    public static void main(String args[]){

        for (int i = 10; i > 0; i--) {
            System.out.print(i + "...");
        }
        System.out.println("F O G O ! ! !");
    }
}
